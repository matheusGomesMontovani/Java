class Operators {
    
    public static void main(String[] arg) {
        float number1 = 12;
        float number2 = 13;

        number1++;
        number2--;

        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number1 / number2);
        System.out.println(number1 % number2);
        System.out.println(number1 == number2);


        System.out.println("---------------------");

        boolean bleach = true;
        boolean naruto = false;

        System.out.println(bleach && naruto);
        System.out.println(bleach || naruto);
    }
}
